#include "3-calc.h"

/**
 * op_add - add intergers
 * @a: first integer
 * @b: second integer
 * Return: sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract integers
 * @a: integer
 * @b: integer
 * Return: result
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply integers
 * @a: integer
 * @b: integer
 * Return: result
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide integers
 * @a: integer
 * @b: integer
 * Return: result
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulus of integers
 * @a: integer
 * @b: integer
 * Return: result
 */

int op_mod(int a, int b)
{
	return (a % b);
}
